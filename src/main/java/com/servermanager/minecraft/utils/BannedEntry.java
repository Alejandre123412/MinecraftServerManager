package com.servermanager.minecraft.utils;

/**
 * Representa una entrada de un jugador baneado,
 * incluyendo UUID, nombre y razón del baneo.
 */
public class BannedEntry {
    /** UUID del jugador baneado */
    public String uuid;

    /** Nombre del jugador baneado */
    public String name;

    /** Razón del baneo */
    public String reason;
}
